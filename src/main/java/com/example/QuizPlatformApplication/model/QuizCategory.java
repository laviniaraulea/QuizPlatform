package com.example.QuizPlatformApplication.model;

/**
 * Enumeration representing the categories for quizzes in the Quiz Platform Application.
 */
public enum QuizCategory {
    /**
     * General knowledge category.
     */
    GENERAL_KNOWLEDGE,
    /**
     * Science and nature category.
     */
    SCIENCE_AND_NATURE,
    /**
     * History category.
     */
    HISTORY,
    /**
     * Food and cooking category.
     */
    FOOD_AND_COOKING,
    /**
     * Art and artists category.
     */
    ART_AND_ARTISTS,
    /**
     * Famous personalities category.
     */
    FAMOUS_PERSONALITIES,
    /**
     * Other category.
     */
    OTHER
}
